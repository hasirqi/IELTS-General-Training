export function buildLearningLexicon(items) {
  return items.map((item) => {
    const verified = item.kind === "chunk" ? verifiedChunkContent[item.term.toLowerCase()] : null;
    if (verified) {
      return {
        ...item,
        cue: `“${item.meaning}”最常用的英语词块是什么？`,
        example: verified[0],
        collocation: verified[1],
        contentStatus: "verified",
      };
    }
    if (item.cue && item.example && item.collocation) return { ...item, contentStatus: "verified" };
    return { ...item, cue: `“${item.meaning}”用英语怎么说？`, contentStatus: "core-only" };
  });
}

export function lexiconQuality(items) {
  const verified = items.filter((item) => item.contentStatus === "verified").length;
  return { total: items.length, verified, coreOnly: items.length - verified };
}
